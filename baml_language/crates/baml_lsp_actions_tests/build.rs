//! Build script that generates LSP tests from test_files/ directory.
//! Each folder becomes a test module with inline assertion tests.

use std::{
    env, fs,
    path::{Path, PathBuf},
};

use proc_macro2::TokenStream;
use quote::{format_ident, quote};
use walkdir::WalkDir;

/// Creates an `include_str!(r"path")` expression.
fn make_include_str(path: &str) -> TokenStream {
    let lit = syn::LitStr::new(path, proc_macro2::Span::call_site());
    quote! {
        include_str!(#lit)
    }
}

fn main() {
    // Watch the test_files directory for changes
    println!("cargo:rerun-if-changed=test_files");

    let out_dir = env::var("OUT_DIR").unwrap();
    let manifest_dir = env::var("CARGO_MANIFEST_DIR").unwrap();

    generate_lsp_tests(&out_dir, &manifest_dir);
}

fn write_formatted_code(path: &Path, code: TokenStream, header: &str) {
    let code_string = code.to_string();
    let syntax_tree = syn::parse_file(&code_string).expect("Failed to parse generated code");
    let formatted = prettyplease::unparse(&syntax_tree);

    let output = format!("{header}\n{formatted}");
    fs::write(path, output).unwrap();
}

// ============================================================================
// LSP test generation
// ============================================================================

struct LspTestFolder {
    name: String,
    #[allow(dead_code)]
    path: PathBuf,
    files: Vec<LspTestFile>,
}

struct LspTestFile {
    name: String,
    #[allow(dead_code)]
    relative_path: PathBuf,
    full_path: PathBuf,
}

/// Discover and generate LSP tests from test_files/ directory.
fn generate_lsp_tests(out_dir: &str, manifest_dir: &str) {
    let test_files_dir = Path::new(manifest_dir).join("test_files");
    let dest_path = Path::new(out_dir).join("generated_lsp_tests.rs");

    let header = "\
// Auto-generated LSP tests from test_files/
// Do not edit this file directly.
";

    if !test_files_dir.exists() {
        fs::write(&dest_path, format!("{header}\n// No LSP tests found\n")).unwrap();
        return;
    }

    let test_folders = discover_lsp_test_folders(&test_files_dir);

    if test_folders.is_empty() {
        fs::write(&dest_path, format!("{header}\n// No LSP tests found\n")).unwrap();
        return;
    }

    let test_modules: TokenStream = test_folders
        .iter()
        .map(|folder| generate_lsp_test_module(folder, manifest_dir))
        .collect();

    write_formatted_code(&dest_path, test_modules, header);
}

fn discover_lsp_test_folders(dir: &Path) -> Vec<LspTestFolder> {
    let mut folders = Vec::new();

    for entry in fs::read_dir(dir).unwrap() {
        let entry = entry.unwrap();
        let path = entry.path();

        if path.is_dir() {
            let name = path.file_name().unwrap().to_str().unwrap().to_string();
            let files = discover_lsp_test_files(&path);

            if !files.is_empty() {
                folders.push(LspTestFolder { name, path, files });
            }
        }
    }

    folders.sort_by(|a, b| a.name.cmp(&b.name));
    folders
}

fn discover_lsp_test_files(dir: &Path) -> Vec<LspTestFile> {
    let mut files = Vec::new();

    for entry in WalkDir::new(dir) {
        let entry = entry.unwrap();
        let path = entry.path();

        if path.extension().and_then(|s| s.to_str()) == Some("baml") {
            let relative_path = path.strip_prefix(dir).unwrap().to_path_buf();
            let name = relative_path
                .to_str()
                .unwrap()
                .replace(['/', '\\'], "_")
                .replace(".baml", "");

            files.push(LspTestFile {
                name,
                relative_path,
                full_path: path.to_path_buf(),
            });
        }
    }

    files.sort_by(|a, b| a.name.cmp(&b.name));
    files
}

fn generate_lsp_test_module(folder: &LspTestFolder, manifest_dir: &str) -> TokenStream {
    let module_name = format_ident!("{}", folder.name.replace('-', "_"));

    let tests: TokenStream = folder
        .files
        .iter()
        .map(|file| generate_lsp_test(file, manifest_dir))
        .collect();

    quote! {
        #[cfg(test)]
        mod #module_name {
            use crate::{parser, runner, updater};
            use std::path::Path;

            #tests
        }
    }
}

fn generate_lsp_test(file: &LspTestFile, _manifest_dir: &str) -> TokenStream {
    let test_name = format_ident!("test_{}", file.name);
    let full_path = file.full_path.display().to_string();
    let default_filename = format!("{}.baml", file.name);
    let include_content = make_include_str(&full_path);

    let full_path_for_update = full_path.clone();

    quote! {
        #[test]
        fn #test_name() {
            let content = #include_content;
            let content = content.replace("\r\n", "\n");

            // Parse the test file
            let parsed = parser::parse_test_file(&content, #default_filename);

            // Run the test
            let result = runner::run_test(&parsed);

            // Check if we should update expectations
            if std::env::var("UPDATE_EXPECT").is_ok() && !result.passed {
                updater::update_test_file(
                    Path::new(#full_path_for_update),
                    &content,
                    &result,
                ).expect("Failed to update test file");

                // Re-run to verify the update worked
                let updated_content = std::fs::read_to_string(#full_path_for_update)
                    .expect("Failed to read updated file");
                let updated_parsed = parser::parse_test_file(&updated_content, #default_filename);
                let updated_result = runner::run_test(&updated_parsed);
                assert!(
                    updated_result.passed,
                    "Test still failed after update:\n{}",
                    updated_result.diff.as_deref().unwrap_or("<no diff>")
                );
            } else {
                assert!(
                    result.passed,
                    "LSP test failed.\n\
                     Run with UPDATE_EXPECT=1 to update expectations.\n\
                     Diff:\n{}",
                    result.diff.as_deref().unwrap_or("<no diff>")
                );
            }
        }
    }
}
