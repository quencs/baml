// Helper function for formatting syntax trees
#[cfg(test)]
fn format_syntax_tree(node: &baml_db::baml_compiler_syntax::SyntaxNode) -> String {
    format_node_recursive(node, 0)
}

#[cfg(test)]
fn format_node_recursive(node: &baml_db::baml_compiler_syntax::SyntaxNode, depth: usize) -> String {
    use baml_db::baml_compiler_syntax::NodeOrToken;

    let mut result = String::new();
    let indent = "  ".repeat(depth);

    result.push_str(&format!("{}{:?}", indent, node.kind()));

    // For leaf nodes (no child nodes), include the text
    if node.first_child().is_none() {
        let text = node.text().to_string().trim().to_string();
        if !text.is_empty() {
            // If text already has quotes, show as-is; otherwise wrap in quotes
            if text.starts_with('"') || text.starts_with("#\"") {
                result.push_str(&format!(" {}", text));
            } else {
                result.push_str(&format!(" \"{}\"", text));
            }
        }
    }

    result.push('\n');

    // Iterate over both nodes and tokens
    for child in node.children_with_tokens() {
        match child {
            NodeOrToken::Node(child_node) => {
                result.push_str(&format_node_recursive(&child_node, depth + 1));
            }
            NodeOrToken::Token(token) => {
                // Show tokens (but skip trivia for readability)
                if !token.kind().is_trivia() {
                    result.push_str(&format!(
                        "{}{:?} \"{}\"\n",
                        "  ".repeat(depth + 1),
                        token.kind(),
                        token.text()
                    ));
                }
            }
        }
    }

    result
}

// Use the shared type_ref_to_str function from baml_compiler_hir
#[cfg(test)]
use baml_compiler_hir::type_ref_to_str as format_type_ref;

// Helper function for formatting HIR items from a specific file
#[cfg(test)]
fn format_hir_file(
    db: &baml_project::ProjectDatabase,
    source_file: baml_db::SourceFile,
    items: &[baml_db::baml_compiler_hir::ItemId],
) -> String {
    use std::fmt::Write;

    use baml_compiler_hir::{function_body, function_signature, template_string_signature};
    use baml_db::baml_compiler_hir::ItemId;

    // Get the ItemTree once and keep it alive for all lookups
    let item_tree = baml_db::baml_compiler_hir::file_item_tree(db, source_file);
    let mut result = String::new();

    for item in items {
        match item {
            ItemId::Function(func_id) => {
                let func = &item_tree[func_id.id(db)];
                let sig = function_signature(db, *func_id);
                let body = function_body(db, *func_id);

                // Format as: function Name(param: Type, ...) -> ReturnType { body }
                write!(result, "function {}(", func.name).unwrap();
                for (i, param) in sig.params.iter().enumerate() {
                    if i > 0 {
                        write!(result, ", ").unwrap();
                    }
                    write!(
                        result,
                        "{}: {}",
                        param.name,
                        format_type_ref(&param.type_ref)
                    )
                    .unwrap();
                }
                writeln!(result, ") -> {} {{", format_type_ref(&sig.return_type)).unwrap();

                // Show body
                match body.as_ref() {
                    baml_db::baml_compiler_hir::FunctionBody::Llm(llm) => {
                        // Show LLM function body as inline config
                        writeln!(result, "  client {}", llm.client).unwrap();
                        // Show prompt as template string
                        writeln!(result, "  prompt #\"").unwrap();
                        for line in llm.prompt.text.lines() {
                            writeln!(result, "    {}", line).unwrap();
                        }
                        writeln!(result, "  \"#").unwrap();
                    }
                    baml_db::baml_compiler_hir::FunctionBody::Expr(expr_body, _source_map) => {
                        // Print as code for readability
                        let code = baml_compiler_hir::body_to_code(expr_body);
                        // Indent the code (skip the outer braces since we already have them)
                        let code = code.trim();
                        if code.starts_with('{') && code.ends_with('}') {
                            // Remove outer braces and print contents.
                            // Inner lines are already indented at 4 spaces
                            // (one indent level) from body_to_code.
                            let inner = &code[1..code.len() - 1];
                            for line in inner.lines() {
                                if !line.trim().is_empty() {
                                    writeln!(result, "{}", line).unwrap();
                                }
                            }
                        } else {
                            for line in code.lines() {
                                writeln!(result, "    {}", line).unwrap();
                            }
                        }
                    }
                    baml_db::baml_compiler_hir::FunctionBody::Missing => {
                        writeln!(result, "    // missing body").unwrap();
                    }
                }

                writeln!(result, "}}").unwrap();
                writeln!(result).unwrap(); // blank line after function
            }
            ItemId::Class(class_id) => {
                let class = &item_tree[class_id.id(db)];
                writeln!(result, "class {} {{", class.name).unwrap();
                for field in &class.fields {
                    writeln!(
                        result,
                        "  {}: {}",
                        field.name,
                        format_type_ref(&field.type_ref)
                    )
                    .unwrap();
                }
                if class.is_dynamic.is_explicit() {
                    writeln!(result, "  @@dynamic").unwrap();
                }
                writeln!(result, "}}").unwrap();
                writeln!(result).unwrap(); // blank line after class
            }
            ItemId::Enum(enum_id) => {
                let enum_def = &item_tree[enum_id.id(db)];
                writeln!(result, "enum {} {{", enum_def.name).unwrap();
                for variant in &enum_def.variants {
                    writeln!(result, "  {}", variant.name).unwrap();
                }
                writeln!(result, "}}").unwrap();
                writeln!(result).unwrap(); // blank line after enum
            }
            ItemId::TypeAlias(alias_id) => {
                let alias = &item_tree[alias_id.id(db)];
                writeln!(
                    result,
                    "type {} = {}",
                    alias.name,
                    format_type_ref(&alias.type_ref)
                )
                .unwrap();
                writeln!(result).unwrap(); // blank line after type alias
            }
            ItemId::Client(client_id) => {
                let client = &item_tree[client_id.id(db)];
                writeln!(result, "client {} {{", client.name).unwrap();
                writeln!(result, "  provider: {}", client.provider).unwrap();
                writeln!(result, "}}").unwrap();
                writeln!(result).unwrap(); // blank line after client
            }
            ItemId::Test(test_id) => {
                let test = &item_tree[test_id.id(db)];
                writeln!(result, "test {} {{", test.name).unwrap();
                if !test.function_refs.is_empty() {
                    writeln!(result, "  functions: {:?}", test.function_refs).unwrap();
                }
                // Format type_builder block if present
                if let Some(ref type_builder) = test.type_builder {
                    writeln!(result, "  type_builder {{").unwrap();
                    for entry in &type_builder.entries {
                        match entry {
                            baml_db::baml_compiler_hir::TypeBuilderEntry::Class(class) => {
                                writeln!(result, "    class {} {{", class.name).unwrap();
                                for field in &class.fields {
                                    writeln!(
                                        result,
                                        "      {}: {}",
                                        field.name,
                                        format_type_ref(&field.type_ref)
                                    )
                                    .unwrap();
                                }
                                writeln!(result, "    }}").unwrap();
                            }
                            baml_db::baml_compiler_hir::TypeBuilderEntry::Enum(enum_def) => {
                                writeln!(result, "    enum {} {{", enum_def.name).unwrap();
                                for variant in &enum_def.variants {
                                    writeln!(result, "      {}", variant.name).unwrap();
                                }
                                writeln!(result, "    }}").unwrap();
                            }
                            baml_db::baml_compiler_hir::TypeBuilderEntry::DynamicClass(class) => {
                                writeln!(result, "    dynamic class {} {{", class.name).unwrap();
                                for field in &class.fields {
                                    writeln!(
                                        result,
                                        "      {}: {}",
                                        field.name,
                                        format_type_ref(&field.type_ref)
                                    )
                                    .unwrap();
                                }
                                writeln!(result, "    }}").unwrap();
                            }
                            baml_db::baml_compiler_hir::TypeBuilderEntry::DynamicEnum(enum_def) => {
                                writeln!(result, "    dynamic enum {} {{", enum_def.name).unwrap();
                                for variant in &enum_def.variants {
                                    writeln!(result, "      {}", variant.name).unwrap();
                                }
                                writeln!(result, "    }}").unwrap();
                            }
                            baml_db::baml_compiler_hir::TypeBuilderEntry::TypeAlias(alias) => {
                                writeln!(
                                    result,
                                    "    type {} = {}",
                                    alias.name,
                                    format_type_ref(&alias.type_ref)
                                )
                                .unwrap();
                            }
                        }
                    }
                    writeln!(result, "  }}").unwrap();
                }
                writeln!(result, "}}").unwrap();
                writeln!(result).unwrap(); // blank line after test
            }
            ItemId::Generator(gen_id) => {
                let generator = &item_tree[gen_id.id(db)];
                writeln!(result, "generator {} {{", generator.name).unwrap();
                if let Some(ref output_type) = generator.output_type {
                    writeln!(result, "  output_type: {}", output_type).unwrap();
                }
                if let Some(ref output_dir) = generator.output_dir {
                    writeln!(result, "  output_dir: {}", output_dir).unwrap();
                }
                if let Some(ref version) = generator.version {
                    writeln!(result, "  version: {}", version).unwrap();
                }
                writeln!(result, "}}").unwrap();
                writeln!(result).unwrap(); // blank line after generator
            }
            ItemId::TemplateString(ts_id) => {
                let ts = &item_tree[ts_id.id(db)];
                let sig = template_string_signature(db, *ts_id);

                // Format as: template_string Name(param: Type, ...)
                write!(result, "template_string {}(", ts.name).unwrap();
                for (i, param) in sig.params.iter().enumerate() {
                    if i > 0 {
                        write!(result, ", ").unwrap();
                    }
                    write!(
                        result,
                        "{}: {}",
                        param.name,
                        format_type_ref(&param.type_ref)
                    )
                    .unwrap();
                }
                write!(result, ")").unwrap();
                writeln!(result).unwrap(); // blank line after template_string
            }
            ItemId::RetryPolicy(rp_loc) => {
                let rp = &item_tree[rp_loc.id(db)];
                writeln!(result, "retry_policy {}", rp.name).unwrap();
                writeln!(result).unwrap();
            }
        }
    }

    result
}
