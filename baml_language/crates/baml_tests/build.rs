//! Build script that generates tests from projects/ directory and benchmarks from benches/.
//! Each folder becomes a test module with comprehensive compiler phase tests.

use std::env;
use std::fs::{self, File};
use std::io::Write;
use std::path::{Path, PathBuf};
use walkdir::WalkDir;

fn main() {
    // Watch the projects and benches directories for changes
    println!("cargo:rerun-if-changed=projects");
    println!("cargo:rerun-if-changed=benches");

    let out_dir = env::var("OUT_DIR").unwrap();
    let manifest_dir = env::var("CARGO_MANIFEST_DIR").unwrap();

    // Generate tests
    generate_tests(&out_dir, &manifest_dir);

    // Generate benchmarks
    generate_benchmarks(&out_dir, &manifest_dir);
}

fn generate_tests(out_dir: &str, manifest_dir: &str) {
    let projects_dir = Path::new(&manifest_dir).join("projects");

    // Discover all projects
    let projects = discover_projects(&projects_dir);

    // Generate test file
    let dest_path = Path::new(&out_dir).join("generated_tests.rs");
    let mut file = File::create(&dest_path).unwrap();

    writeln!(file, "// Auto-generated tests from projects/").unwrap();
    writeln!(file, "// Do not edit this file directly").unwrap();
    writeln!(file).unwrap();

    for project in projects {
        generate_project_tests(&mut file, &project).unwrap();
    }
}

fn generate_benchmarks(out_dir: &str, manifest_dir: &str) {
    let benches_dir = Path::new(&manifest_dir).join("benches");

    if !benches_dir.exists() {
        // No benchmarks to generate - create empty file
        let dest_path = Path::new(&out_dir).join("generated_benchmarks.rs");
        let mut file = File::create(&dest_path).unwrap();
        writeln!(file, "// No benchmarks found").unwrap();
        return;
    }

    // Discover all benchmarks
    let mut benchmarks = Vec::new();

    // Discover incremental benchmarks
    let incremental_dir = benches_dir.join("incremental");
    if incremental_dir.exists() {
        discover_incremental_benchmarks(&incremental_dir, &mut benchmarks);
    }

    // Discover scale benchmarks
    let scale_dir = benches_dir.join("scale");
    if scale_dir.exists() {
        discover_scale_benchmarks(&scale_dir, &mut benchmarks);
    }

    // Generate benchmark file
    let dest_path = Path::new(&out_dir).join("generated_benchmarks.rs");
    let mut file = File::create(&dest_path).unwrap();

    writeln!(file, "// Auto-generated benchmarks from benches/").unwrap();
    writeln!(file, "// Do not edit this file directly").unwrap();
    writeln!(file).unwrap();
    writeln!(file, "// Note: divan::Bencher, baml_db::*, and divan::black_box are already imported in the main file").unwrap();
    writeln!(file).unwrap();

    let mut incremental_bench_names = Vec::new();
    let mut scale_bench_names = Vec::new();

    for benchmark in &benchmarks {
        match benchmark {
            Benchmark::IncrementalMultiFile {
                name,
                before_files,
                after_files,
                delete_files,
            } => {
                let fn_name = format!("bench_incremental_{}", name.replace("-", "_"));
                incremental_bench_names.push(fn_name.clone());
                generate_incremental_benchmark(
                    &mut file,
                    name,
                    before_files.clone(),
                    after_files.clone(),
                    delete_files.clone(),
                )
                .unwrap();
            }
            Benchmark::Scale { name, path } => {
                let fn_name = format!("bench_scale_{}", name.replace("-", "_"));
                scale_bench_names.push(fn_name.clone());
                generate_scale_benchmark(&mut file, name, path.clone()).unwrap();
            }
        }
    }

    // With divan, benchmarks are automatically discovered via the #[divan::bench] attribute
    // No need for explicit benchmark groups
}

// Test-related structures and functions
struct TestProject {
    name: String,
    #[allow(dead_code)]
    path: PathBuf,
    files: Vec<BamlFile>,
}

struct BamlFile {
    name: String,
    relative_path: PathBuf,
    full_path: PathBuf,
}

fn discover_projects(projects_dir: &Path) -> Vec<TestProject> {
    let mut projects = Vec::new();

    if !projects_dir.exists() {
        return projects;
    }

    for entry in fs::read_dir(projects_dir).unwrap() {
        let entry = entry.unwrap();
        let path = entry.path();

        if !path.is_dir() {
            continue;
        }

        let name = path.file_name().unwrap().to_str().unwrap().to_string();

        let files = discover_baml_files(&path);

        if !files.is_empty() {
            projects.push(TestProject { name, path, files });
        }
    }

    projects.sort_by(|a, b| a.name.cmp(&b.name));
    projects
}

fn discover_baml_files(dir: &Path) -> Vec<BamlFile> {
    let mut files = Vec::new();

    for entry in WalkDir::new(dir) {
        let entry = entry.unwrap();
        let path = entry.path();

        if path.extension().and_then(|s| s.to_str()) == Some("baml") {
            let relative_path = path.strip_prefix(dir).unwrap().to_path_buf();

            // Create safe test name from path
            let name = relative_path
                .to_str()
                .unwrap()
                .replace(['/', '\\'], "_")
                .replace(".baml", "");

            files.push(BamlFile {
                name,
                relative_path,
                full_path: path.to_path_buf(),
            });
        }
    }

    files.sort_by(|a, b| a.relative_path.cmp(&b.relative_path));
    files
}

fn generate_project_tests(file: &mut File, project: &TestProject) -> std::io::Result<()> {
    let module_name = project.name.replace("-", "_");

    writeln!(file, "#[cfg(test)]")?;
    writeln!(file, "mod {} {{", module_name)?;
    writeln!(file, "    use baml_db::*;")?;
    writeln!(file, "    use baml_db::baml_lexer;")?;
    writeln!(file, "    use baml_db::baml_parser;")?;
    writeln!(file, "    use baml_db::baml_hir;")?;
    writeln!(file, "    use baml_db::baml_thir;")?;
    writeln!(file, "    use baml_db::baml_codegen;")?;
    writeln!(file, "    use baml_db::Diagnostic;")?;
    writeln!(file, "    use insta::{{assert_snapshot, with_settings}};")?;
    writeln!(file, "    use std::fmt::Write;")?;

    // Get the manifest dir at compile time and use it in the test
    let manifest_dir = env::var("CARGO_MANIFEST_DIR").unwrap();
    // Replace backslashes with forward slashes for cross-platform compatibility
    let manifest_dir = manifest_dir.replace('\\', "/");
    writeln!(
        file,
        "    const SNAPSHOT_PATH: &str = \"{}/snapshots\";",
        manifest_dir
    )?;
    writeln!(file)?;

    // Generate lexer tests for each file
    for baml_file in &project.files {
        generate_lexer_test(file, project, baml_file)?;
    }

    // Generate parser tests for each file
    for baml_file in &project.files {
        generate_parser_test(file, project, baml_file)?;
    }

    // Generate project-wide tests
    generate_hir_test(file, project)?;
    generate_thir_test(file, project)?;
    generate_diagnostics_test(file, project)?;
    generate_codegen_test(file, project)?;

    writeln!(file, "}}")?;
    writeln!(file)?;

    Ok(())
}

fn generate_lexer_test(
    file: &mut File,
    project: &TestProject,
    baml_file: &BamlFile,
) -> std::io::Result<()> {
    let test_name = format!("test_01_lexer_{}", baml_file.name);
    let snapshot_name = format!("{}__01_lexer__{}", project.name, baml_file.name);

    writeln!(file, "    #[test]")?;
    writeln!(file, "    fn {}() {{", test_name)?;
    writeln!(
        file,
        "        let content = include_str!(r\"{}\");",
        baml_file.full_path.display()
    )?;
    writeln!(
        file,
        "        // Normalize line endings for cross-platform compatibility"
    )?;
    writeln!(
        file,
        "        let content = content.replace(\"\\r\\n\", \"\\n\");"
    )?;
    writeln!(file, "        let mut db = RootDatabase::new();")?;
    writeln!(
        file,
        "        let source_file = db.add_file(\"{}\", &content);",
        baml_file.relative_path.display()
    )?;
    writeln!(
        file,
        "        let tokens = baml_lexer::lex_file(&db, source_file);"
    )?;
    writeln!(file)?;
    writeln!(file, "        // Format tokens as readable text")?;
    writeln!(file, "        let mut output = String::new();")?;
    writeln!(file, "        for token in tokens.iter() {{")?;
    writeln!(file, "            if !matches!(token.kind,")?;
    writeln!(file, "                baml_lexer::TokenKind::Whitespace |")?;
    writeln!(file, "                baml_lexer::TokenKind::Newline |")?;
    writeln!(file, "                baml_lexer::TokenKind::LineComment |")?;
    writeln!(file, "                baml_lexer::TokenKind::BlockComment")?;
    writeln!(file, "            ) {{")?;
    writeln!(
        file,
        "                writeln!(output, \"{{:?}} {{:?}}\", token.kind, token.text).unwrap();"
    )?;
    writeln!(file, "            }}")?;
    writeln!(file, "        }}")?;
    writeln!(file)?;
    writeln!(
        file,
        "        with_settings!({{snapshot_path => SNAPSHOT_PATH}}, {{"
    )?;
    writeln!(
        file,
        "            assert_snapshot!(\"{}\", output);",
        snapshot_name
    )?;
    writeln!(file, "        }});")?;
    writeln!(file, "    }}")?;
    writeln!(file)?;

    Ok(())
}

