// Benchmark-related structures and functions
enum Benchmark {
    IncrementalMultiFile {
        name: String,
        before_files: Vec<PathBuf>,
        after_files: Vec<PathBuf>,
        delete_files: Vec<String>,
    },
    Scale {
        name: String,
        path: PathBuf,
    },
}

fn discover_incremental_benchmarks(dir: &Path, benchmarks: &mut Vec<Benchmark>) {
    for entry in fs::read_dir(dir).unwrap() {
        let entry = entry.unwrap();
        let path = entry.path();
        if !path.is_dir() {
            continue;
        }

        let before_dir = path.join("before");
        let after_dir = path.join("after");

        if before_dir.exists() && after_dir.exists() {
            let name = path.file_name().unwrap().to_str().unwrap().to_string();

            // Collect all files from before/
            let before_files = collect_baml_files(&before_dir);

            // Collect changes from after/ (including .delete markers)
            let mut after_files = Vec::new();
            let mut delete_files = Vec::new();

            for entry in WalkDir::new(&after_dir) {
                let entry = entry.unwrap();
                let path = entry.path();

                if path.extension().and_then(|s| s.to_str()) == Some("baml") {
                    after_files.push(path.to_path_buf());
                } else if let Some(filename) = path.file_name().and_then(|s| s.to_str())
                    && filename.ends_with(".baml.delete")
                {
                    // Extract the original filename
                    let original = filename.strip_suffix(".delete").unwrap();
                    delete_files.push(original.to_string());
                }
            }

            benchmarks.push(Benchmark::IncrementalMultiFile {
                name,
                before_files,
                after_files,
                delete_files,
            });
        }
    }
}

fn discover_scale_benchmarks(dir: &Path, benchmarks: &mut Vec<Benchmark>) {
    for entry in WalkDir::new(dir) {
        let entry = entry.unwrap();
        let path = entry.path();

        if path.extension().and_then(|s| s.to_str()) == Some("baml") {
            let name = path.file_stem().unwrap().to_str().unwrap().to_string();
            benchmarks.push(Benchmark::Scale {
                name,
                path: path.to_path_buf(),
            });
        }
    }
}

fn collect_baml_files(dir: &Path) -> Vec<PathBuf> {
    let mut files = Vec::new();

    for entry in WalkDir::new(dir) {
        let entry = entry.unwrap();
        let path = entry.path();

        if path.extension().and_then(|s| s.to_str()) == Some("baml") {
            files.push(path.to_path_buf());
        }
    }

    files.sort();
    files
}

fn generate_incremental_benchmark(
    file: &mut File,
    name: &str,
    before_files: Vec<PathBuf>,
    after_files: Vec<PathBuf>,
    delete_files: Vec<String>,
) -> std::io::Result<()> {
    let fn_name = format!("bench_incremental_{}", name.replace("-", "_"));

    writeln!(file, "#[divan::bench]")?;
    writeln!(file, "fn {}(bencher: divan::Bencher) {{", fn_name)?;

    // Include all before files
    for (i, path) in before_files.iter().enumerate() {
        writeln!(
            file,
            "    let before_{}_raw = include_str!(r\"{}\");",
            i,
            path.display()
        )?;
        writeln!(
            file,
            "    let before_{} = before_{}_raw.replace(\"\\r\\n\", \"\\n\");",
            i, i
        )?;
    }

    // Include sparse after files (not .delete files)
    for (i, path) in after_files.iter().enumerate() {
        writeln!(
            file,
            "    let after_{}_raw = include_str!(r\"{}\");",
            i,
            path.display()
        )?;
        writeln!(
            file,
            "    let after_{} = after_{}_raw.replace(\"\\r\\n\", \"\\n\");",
            i, i
        )?;
    }

    writeln!(file, "    ")?;
    writeln!(file, "    bencher.bench_local(|| {{")?;
    writeln!(file, "        let mut db = RootDatabase::new();")?;
    writeln!(
        file,
        "        let root = db.set_project_root(std::path::PathBuf::from(\".\"));"
    )?;
    writeln!(file)?;

    // Initial compilation - load all before files
    writeln!(file, "        // Initial compilation")?;
    for (i, path) in before_files.iter().enumerate() {
        let rel_path = path.file_name().unwrap().to_str().unwrap();
        writeln!(
            file,
            "        db.add_file(\"{}\", &before_{});",
            rel_path, i
        )?;
    }
    writeln!(
        file,
        "        let _ = baml_hir::project_items(&db, root);  // Full compilation"
    )?;
    writeln!(file)?;

    // Apply incremental changes by re-adding files with new content
    writeln!(
        file,
        "        // Apply incremental changes (re-add files with new content)"
    )?;

    // Handle updates and new files
    for (after_idx, after_path) in after_files.iter().enumerate() {
        let after_filename = after_path.file_name().unwrap().to_str().unwrap();
        // Simply add the file again - Salsa will handle incremental compilation
        writeln!(
            file,
            "        db.add_file(\"{}\", &after_{});  // Updated/New file",
            after_filename, after_idx
        )?;
    }

    // Note: Since there's no remove_file, we can't test deletion scenarios
    if !delete_files.is_empty() {
        writeln!(
            file,
            "        // Note: File deletions cannot be tested without remove_file API"
        )?;
    }

    writeln!(
        file,
        "        let _ = black_box(baml_hir::project_items(&db, root));  // Incremental compilation"
    )?;
    writeln!(file, "    }});")?;
    writeln!(file, "}}")?;
    writeln!(file)?;

    Ok(())
}

fn generate_scale_benchmark(file: &mut File, name: &str, path: PathBuf) -> std::io::Result<()> {
    let fn_name = format!("bench_scale_{}", name.replace("-", "_"));

    writeln!(file, "#[divan::bench]")?;
    writeln!(file, "fn {}(bencher: divan::Bencher) {{", fn_name)?;
    writeln!(
        file,
        "    let content_raw = include_str!(r\"{}\");",
        path.display()
    )?;
    writeln!(
        file,
        "    let content = content_raw.replace(\"\\r\\n\", \"\\n\");"
    )?;
    writeln!(file, "    ")?;
    writeln!(file, "    bencher.bench_local(|| {{")?;
    writeln!(file, "        let mut db = RootDatabase::new();")?;
    writeln!(
        file,
        "        let root = db.set_project_root(std::path::PathBuf::from(\".\"));"
    )?;
    writeln!(file, "        db.add_file(\"{}.baml\", &content);", name)?;
    writeln!(
        file,
        "        let _ = black_box(baml_hir::project_items(&db, root));"
    )?;
    writeln!(file, "    }});")?;
    writeln!(file, "}}")?;
    writeln!(file)?;

    Ok(())
}
