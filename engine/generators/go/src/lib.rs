mod r#type;
mod utils;

#[derive(Default)]
pub struct GoLanguageFeatures;

impl LanguageFeatures for GoLanguageFeatures {
    const CONTENT_PREFIX: &'static str = r#"
// ----------------------------------------------------------------------------
//
//  Welcome to Baml! To use this generated code, please run the following:
//
//  $ go get github.com/boundaryml/baml
//
// ----------------------------------------------------------------------------

// This file was generated by BAML: please do not edit it. Instead, edit the
// BAML files and re-generate this code using: baml-cli generate
// You can install baml-cli with:
//  $ go install github.com/boundaryml/baml/baml-cli
        "#;

    fn generate_sdk_files(
        &self,
        collector: &mut FileCollector<Self>,
        ir: &IntermediateRepr,
        args: &GeneratorArgs,
    ) -> Result<(), anyhow::Error> {
        let Some(go_mod_name) = &args.client_package_name else {
            anyhow::bail!("Go client package name is required");
        };

        let pkg = r#type::Package::new("baml_client");
        collector.add_file(
            "client.go",
            render_functions(
                ir.functions
                    .iter()
                    .map(|f| ir_to_go::functions::ir_function_to_go(f, &pkg))
                    .collect::<Vec<_>>()
                    .as_slice(),
                &pkg,
                go_mod_name,
            )?,
        );

        let pkg = r#type::Package::new("baml_client.types");
        collector.add_file("types/types.go", render_go_types(&[], &[], &[], &pkg)?);

        let pkg = r#type::Package::new("baml_client.stream_types");
        collector.add_file(
            "stream_types/stream_types.go",
            render_go_stream_types(&[], &[], &pkg)?,
        );

        Ok(())
    }
}

#[cfg(test)]
mod tests {
    use internal_baml_core::ir::repr::make_test_ir;

    use super::*;
    use anyhow::Result;
    use std::{path::PathBuf, process::Command};

    fn args() -> GeneratorArgs {
        GeneratorArgs {
            client_package_name: Some("sample".to_string()),
            output_dir_relative_to_baml_src: PathBuf::from("./sample/baml_client"),
            baml_src_dir: PathBuf::from("."),
            inlined_file_map: Default::default(),
            version: "0.1.0".to_string(),
            no_version_check: true,
            default_client_mode: baml_types::GeneratorDefaultClientMode::Async,
            on_generate: vec!["gofmt -w . && goimports -w .".to_string()],
            client_type: baml_types::GeneratorOutputType::Go,
            module_format: None,
        }
    }

    #[test]
    fn test_go_language_features() -> Result<()> {
        let features = GoLanguageFeatures::default();
        let ir = make_test_ir(
            r##"
            function Foo(x: int) -> int | string {
                client "openai/gpt-4o"
                prompt #"you are a helpful assistant"#
            }

            test FooTest {
                functions [Foo]
                args {
                    x 1
                }
            }
        "##,
        )?;
        let args = args();
        features.generate_sdk(&ir, &args)?;

        // print PWD
        let pwd = std::env::current_dir().unwrap();
        let sample_dir = pwd.join("sample");

        for cmd_str in args.on_generate {
            let mut cmd = Command::new("sh");
            cmd.args(&["-c", &cmd_str]);
            cmd.current_dir(&sample_dir);
            println!("Running command: {:#?}", cmd);
            let output = cmd.output().expect("failed to run command");
            assert!(
                output.status.success(),
                "{}",
                String::from_utf8_lossy(&output.stderr)
            );
        }

        Ok(())
    }
}
