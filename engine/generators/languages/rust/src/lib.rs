use std::{collections::HashSet, sync::Arc};

use anyhow::Result;
use askama::Template;
use baml_types::{ir_type::TypeGeneric, TypeValue};
use dir_writer::{FileCollector, GeneratorArgs, LanguageFeatures};
use internal_baml_core::ir::repr::IntermediateRepr;

/// A collection of recursive cycles, where each cycle is a set of class names
/// that reference each other (directly or indirectly).
pub type RecursiveCycles = Vec<HashSet<String>>;

mod functions;
mod generated_types;
mod ir_to_rust;
mod package;
mod templates;
mod test_macros;
mod r#type;
mod utils;

pub use generated_types::*;
pub use ir_to_rust::*;
pub use package::*;
pub use r#type::*;

#[derive(Default)]
pub struct RustLanguageFeatures;

impl LanguageFeatures for RustLanguageFeatures {
    const CONTENT_PREFIX: &'static str = r#"
// This file was generated by BAML: do not edit it.
// Instead, edit the BAML source files.
//
// Learn more at https://docs.boundaryml.com
"#;

    fn name() -> &'static str {
        "rust"
    }

    fn generate_sdk_files(
        &self,
        collector: &mut FileCollector<Self>,
        ir: Arc<IntermediateRepr>,
        args: &GeneratorArgs,
    ) -> Result<()> {
        use generated_types::{ClassRustRendered, TypeAliasRustRendered, UnionRustRendered};

        let pkg = package::CurrentRenderPackage::new("baml_client", ir.clone());

        // Collect recursive cycles from IR for Box<T> wrapping
        // Each cycle is a set of class names that reference each other
        let recursive_cycles: RecursiveCycles = ir
            .finite_recursive_cycles()
            .iter()
            .map(|cycle| cycle.iter().cloned().collect())
            .collect();

        // Phase 1: Source map
        let file_map = args.file_map_as_json_string()?;
        let source_map = templates::SourceMapTemplate { files: &file_map };
        collector.add_file("baml_source_map.rs", source_map.render()?)?;

        // Phase 2: Runtime
        let runtime = templates::RuntimeTemplate { pkg: &pkg };
        collector.add_file("runtime.rs", runtime.render()?)?;

        // Phase 3: Convert IR to Rust types (non-streaming)
        let classes: Vec<_> = ir
            .walk_classes()
            .map(|c| ir_to_rust::classes::ir_class_to_rust(c.item, &pkg, &recursive_cycles))
            .collect();

        // Stream classes use streaming type conversion (handles StreamState, etc.)
        let stream_classes: Vec<_> = ir
            .walk_classes()
            .map(|c| ir_to_rust::classes::ir_class_to_rust_stream(c.item, &pkg, &recursive_cycles))
            .collect();

        let enums: Vec<_> = ir
            .walk_enums()
            .map(|e| ir_to_rust::enums::ir_enum_to_rust(e.item, &pkg))
            .collect();

        let mut unions: Vec<_> = ir
            .walk_all_non_streaming_unions()
            .flat_map(|t| ir_to_rust::unions::ir_union_to_rust(&t, &pkg, &recursive_cycles))
            .collect();
        unions.sort_by_key(|u| u.name.clone());
        unions.dedup_by_key(|u| u.name.clone());

        // Stream unions
        let mut stream_unions: Vec<_> = ir
            .walk_all_streaming_unions()
            .flat_map(|t| ir_to_rust::unions::ir_union_to_rust_stream(&t, &pkg, &recursive_cycles))
            .collect();
        stream_unions.sort_by_key(|u| u.name.clone());
        stream_unions.dedup_by_key(|u| u.name.clone());

        // key-value pair of what type to drop from the cycle for any given type
        let invalid_cycles = ir
            .structural_recursive_alias_cycles()
            .iter()
            .filter(|&cycle| {
                // find all cycles considered_invalid in go
                cycle.iter().all(|(_, field_type)| {
                    // must have at least one non-recursive type
                    field_type
                        .find_if(
                            &|t| match t {
                                TypeGeneric::Class { .. } => true,
                                TypeGeneric::Enum { .. } => true,
                                TypeGeneric::Literal(..) => true,
                                TypeGeneric::Primitive(TypeValue::Null, ..) => false,
                                TypeGeneric::Primitive(..) => true,
                                _ => false,
                            },
                            true,
                        )
                        .is_empty()
                })
            })
            .flat_map(|cycle| {
                let keys = cycle.keys().cloned().collect::<Vec<_>>();
                let first_key = keys[0].clone();
                keys.into_iter().map(move |k| (k, first_key.clone()))
            })
            .collect::<baml_types::BamlMap<_, _>>();

        let type_aliases = ir.walk_type_aliases().collect::<Vec<_>>();
        let mut type_aliases_rust: Vec<_> = type_aliases
            .iter()
            .map(|c| {
                ir_to_rust::type_aliases::ir_type_alias_to_rust(
                    c.item,
                    &pkg,
                    invalid_cycles.get(&c.elem().name),
                )
            })
            .collect();
        type_aliases_rust.sort_by(|a, b| a.name.cmp(&b.name));

        let mut type_aliases_rust_stream: Vec<_> = type_aliases
            .iter()
            .map(|c| {
                ir_to_rust::type_aliases::ir_type_alias_to_rust_stream(
                    c.item,
                    &pkg,
                    invalid_cycles.get(&c.elem().name),
                )
            })
            .collect();
        type_aliases_rust_stream.sort_by(|a, b| a.name.cmp(&b.name));

        // Get functions
        let functions: Vec<_> = ir
            .walk_functions()
            .map(|f| ir_to_rust::rust_functions::ir_function_to_rust(f.item, &pkg))
            .collect();

        // Phase 4: Generate types/ directory
        pkg.set("baml_client.types");

        // Convert to rendered versions with pre-computed type strings
        let classes_rendered: Vec<_> = classes
            .iter()
            .map(|c| ClassRustRendered::from_class(c, &pkg))
            .collect();
        let unions_rendered: Vec<_> = unions
            .iter()
            .map(|u| UnionRustRendered::from_union(u, &pkg))
            .collect();
        let type_aliases_rendered: Vec<_> = type_aliases_rust
            .iter()
            .map(|a| TypeAliasRustRendered::from_alias(a, &pkg))
            .collect();

        let types_mod = templates::TypesModTemplate {
            classes: &classes_rendered,
            enums: &enums,
            unions: &unions_rendered,
            pkg: &pkg,
        };
        collector.add_file("types/mod.rs", types_mod.render()?)?;

        let classes_tmpl = templates::ClassesTemplate {
            classes: &classes_rendered,
            pkg: &pkg,
        };
        collector.add_file("types/classes.rs", classes_tmpl.render()?)?;

        let enums_tmpl = templates::EnumsTemplate {
            enums: &enums,
            pkg: &pkg,
        };
        collector.add_file("types/enums.rs", enums_tmpl.render()?)?;

        let unions_tmpl = templates::UnionsTemplate {
            unions: &unions_rendered,
            pkg: &pkg,
        };
        collector.add_file("types/unions.rs", unions_tmpl.render()?)?;

        let aliases_tmpl = templates::TypeAliasesTemplate {
            type_aliases: &type_aliases_rendered,
            pkg: &pkg,
        };
        collector.add_file("types/type_aliases.rs", aliases_tmpl.render()?)?;

        // Phase 5: Generate stream_types/ directory
        pkg.set("baml_client.stream_types");

        // Convert stream classes/unions to rendered versions
        let stream_classes_rendered: Vec<_> = stream_classes
            .iter()
            .map(|c| ClassRustRendered::from_class(c, &pkg))
            .collect();
        let stream_unions_rendered: Vec<_> = stream_unions
            .iter()
            .map(|u| UnionRustRendered::from_union(u, &pkg))
            .collect();

        let stream_types_mod = templates::StreamTypesModTemplate {
            classes: &stream_classes_rendered,
            unions: &stream_unions_rendered,
            pkg: &pkg,
        };
        collector.add_file("stream_types/mod.rs", stream_types_mod.render()?)?;

        let stream_classes_tmpl = templates::StreamClassesTemplate {
            classes: &stream_classes_rendered,
            pkg: &pkg,
        };
        collector.add_file("stream_types/classes.rs", stream_classes_tmpl.render()?)?;

        let stream_unions_tmpl = templates::StreamUnionsTemplate {
            unions: &stream_unions_rendered,
            pkg: &pkg,
        };
        collector.add_file("stream_types/unions.rs", stream_unions_tmpl.render()?)?;

        let stream_aliases_rendered: Vec<_> = type_aliases_rust_stream
            .iter()
            .map(|a| TypeAliasRustRendered::from_alias(a, &pkg))
            .collect();
        let stream_aliases_tmpl = templates::StreamTypeAliasesTemplate {
            type_aliases: &stream_aliases_rendered,
            pkg: &pkg,
        };
        collector.add_file(
            "stream_types/type_aliases.rs",
            stream_aliases_tmpl.render()?,
        )?;

        // Phase 6: Generate functions/ directory
        pkg.set("baml_client.functions");

        let fn_mod = templates::FunctionsModTemplate {
            functions: &functions,
            pkg: &pkg,
        };
        collector.add_file("functions/mod.rs", fn_mod.render()?)?;

        let fn_sync_client = templates::FunctionsSyncClientTemplate {
            functions: &functions,
            pkg: &pkg,
        };
        collector.add_file("functions/sync_client.rs", fn_sync_client.render()?)?;

        let fn_async_client = templates::FunctionsAsyncClientTemplate {
            functions: &functions,
            pkg: &pkg,
        };
        collector.add_file("functions/async_client.rs", fn_async_client.render()?)?;

        // Phase 7: Generate type_builder/ directory
        pkg.set("baml_client.type_builder");

        let tb_mod = templates::TypeBuilderModTemplate {
            classes: &classes_rendered,
            enums: &enums,
            pkg: &pkg,
        };
        collector.add_file("type_builder/mod.rs", tb_mod.render()?)?;

        let tb_classes = templates::TypeBuilderClassesTemplate {
            classes: &classes_rendered,
            pkg: &pkg,
        };
        collector.add_file("type_builder/classes.rs", tb_classes.render()?)?;

        let tb_enums = templates::TypeBuilderEnumsTemplate {
            enums: &enums,
            pkg: &pkg,
        };
        collector.add_file("type_builder/enums.rs", tb_enums.render()?)?;

        // Phase 8: Generate root mod.rs
        pkg.set("baml_client");

        let mod_tmpl = templates::ModTemplate {
            pkg: &pkg,
            prefer_async: args.default_client_mode == baml_types::GeneratorDefaultClientMode::Async,
        };
        collector.add_file("mod.rs", mod_tmpl.render()?)?;

        Ok(())
    }
}

#[cfg(test)]
mod rust_tests {
    use test_harness::{create_code_gen_test_suites, TestLanguageFeatures};

    impl TestLanguageFeatures for crate::RustLanguageFeatures {
        fn test_name() -> &'static str {
            "rust"
        }
    }
    create_code_gen_test_suites!(crate::RustLanguageFeatures);
}

#[cfg(test)]
mod tests {
    #[test]
    fn test_name() {
        use std::str::FromStr;

        use dir_writer::LanguageFeatures;

        let gen_type =
            baml_types::GeneratorOutputType::from_str(crate::RustLanguageFeatures::name())
                .expect("RustLanguageFeatures name should be a valid GeneratorOutputType");
        assert_eq!(gen_type, baml_types::GeneratorOutputType::Rust);
    }
}
