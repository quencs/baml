#[derive(Default)]
pub struct RustLanguageFeatures;

impl LanguageFeatures for RustLanguageFeatures {
    const CONTENT_PREFIX: &'static str = r#"
// ----------------------------------------------------------------------------
//
//  Welcome to Baml! To use this generated code, please run the following:
//
//  $ cargo add baml-client
//
// ----------------------------------------------------------------------------

// This file was generated by BAML: please do not edit it. Instead, edit the
// BAML files and re-generate this code using: baml-cli generate
// You can install baml-cli with:
//  $ cargo install baml-cli
        "#;

    fn name() -> &'static str {
        "rust"
    }

    fn generate_sdk_files(
        &self,
        collector: &mut FileCollector<Self>,
        ir: std::sync::Arc<IntermediateRepr>,
        args: &GeneratorArgs,
    ) -> Result<(), anyhow::Error> {
        let pkg = package::CurrentRenderPackage::new("baml_client", ir.clone());
        let file_map = args.file_map_as_json_string()?;
        
        // Generate core files
        collector.add_file("source_map.rs", render_source_files(file_map)?)?;
        collector.add_file("runtime.rs", render_runtime_code(&pkg)?)?;
        collector.add_file("lib.rs", render_lib_rs(&pkg)?)?;
        collector.add_file("Cargo.toml", render_cargo_toml()?)?;

        // Generate function clients
        let functions = ir
            .functions
            .iter()
            .map(|f| ir_to_rust::functions::ir_function_to_rust(f, &pkg))
            .collect::<Vec<_>>();
        collector.add_file("functions.rs", render_functions(&functions, &pkg)?)?;

        // Generate types
        let rust_classes = ir
            .walk_classes()
            .map(|c| ir_to_rust::classes::ir_class_to_rust(c.item, &pkg))
            .collect::<Vec<_>>();
        let enums = ir
            .walk_enums()
            .map(|e| ir_to_rust::enums::ir_enum_to_rust(e.item, &pkg))
            .collect::<Vec<_>>();
        let unions = {
            let mut unions = ir
                .walk_all_non_streaming_unions()
                .filter_map(|t| ir_to_rust::unions::ir_union_to_rust(&t, &pkg))
                .collect::<Vec<_>>();
            unions.sort_by_key(|u| u.name.clone());
            unions.dedup_by_key(|u| u.name.clone());
            unions
        };

        collector.add_file("types.rs", render_rust_types(&rust_classes, &enums, &unions, &pkg)?)?;

        Ok(())
    }
}

fn render_lib_rs(pkg: &package::CurrentRenderPackage) -> Result<String, anyhow::Error> {
    let template = r#"//! BAML Generated Rust Client
//! 
//! This crate provides a type-safe Rust client for your BAML functions.

pub mod types;
pub mod functions;
pub mod runtime;

// Re-exports for convenience
pub use types::*;
pub use functions::*;
pub use runtime::{BamlRuntime, BamlClient};

// Common types
pub use baml_client_rust::{StreamState, Checked, BamlError, BamlResult};
"#;
    Ok(template.to_string())
}

fn render_cargo_toml() -> Result<String, anyhow::Error> {
    let template = r#"[package]
name = "baml-client"
version = "0.1.0"
edition = "2021"

[dependencies]
baml-client-rust = "0.1.0"
tokio = { version = "1.0", features = ["full"] }
serde = { version = "1.0", features = ["derive"] }
serde_json = "1.0"
anyhow = "1.0"
"#;
    Ok(template.to_string())
}

